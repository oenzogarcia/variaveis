let x = 9, y = 3

resultado = x + y // A soma de 9 e 3 é 12
console.log(resultado)
console.log(`A soma de ${x} e ${y} é ${resultado}`)

resultado = x - y // A subtração de 9 e 3 é 6
console.log(resultado)
console.log(`A subtração de ${x} e ${y} é ${resultado}`)

resultado = x * y // A multiplicação de 9 e 3 é 27
console.log(resultado)
console.log(`A multiplicação de ${x} e ${y} é ${resultado}`)

resultado = x / y // A divisão de 9 e 3 é 3
console.log(resultado)
console.log(`A divisão de ${x} e ${y} é ${resultado}`)

resultado = x * (x + 7) - 32 // 112
